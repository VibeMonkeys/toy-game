// 퀴즈 유틸리티 함수
export class QuizUtils {
    // ID로 퀴즈 찾기
    static getQuizById(id) {
        return QUIZ_DATA.find(quiz => quiz.id === id);
    }

    // NPC별 퀴즈 가져오기
    static getQuizzesByNPC(npcName) {
        const quizIds = NPC_QUIZ_MAPPING[npcName] || [];
        return quizIds.map(id => this.getQuizById(id)).filter(quiz => quiz);
    }

    // 카테고리별 퀴즈 가져오기
    static getQuizzesByCategory(category) {
        const quizIds = CATEGORY_QUIZ_MAPPING[category] || [];
        return quizIds.map(id => this.getQuizById(id)).filter(quiz => quiz);
    }

    // 랜덤 퀴즈 선택
    static getRandomQuizFromNPC(npcName) {
        const quizzes = this.getQuizzesByNPC(npcName);
        if (quizzes.length === 0) return null;

        const randomIndex = Math.floor(Math.random() * quizzes.length);
        return quizzes[randomIndex];
    }

    // 난이도별 퀴즈 필터링
    static getQuizzesByDifficulty(difficulty) {
        return QUIZ_DATA.filter(quiz => quiz.difficulty === difficulty);
    }

    // 모든 퀴즈 데이터 반환
    static getAllQuizzes() {
        return QUIZ_DATA;
    }
}